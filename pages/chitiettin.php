<p><em>Reuters</em> dẫn lời Andriy Lysenko, phát ngôn viên Hội đồng bảo an Ukraine, cho biết quân đội nước đã giành được Torez và Shakhtarsk, hai thị trấn trước đó bị phiến quân chiếm đóng. </p>
<p>Quân chính phủ cũng tái chiếm Savur Mogila, một phần chiến lược của vùng đất cách nơi chiếc máy bay của Malaysia Airlines lao xuống khoảng 30 km. Kiev sẽ tiếp tục tiến công vào Snezhnoye và Pervomaisk, hai thị trấn đều nằm sát những cánh đồng lúa mỳ và hoa hướng dương chứa nhiều mảnh vỡ của máy bay .</p>
<p>Gorlovka, nằm ở phía bắc thành trì Donetsk của phiến quân, cũng là một trong các mục tiêu của Kiev. Theo giới chức địa phương, quân đội Ukraine đã nã pháo vào các khu dân cư, đường dây điện và một đường ống khí đốt. Ít nhất ba dân thường đã thiệt mạng trong cuộc xung đột giữa hai bên hôm nay.</p>
<p>Lực lượng ly khai hiện vẫn kiểm soát địa điểm chiếc máy bay MH17 bị rơi. Ông Lysenko cho biết Kiev đang cố gắng tiếp cận khu vực này và đẩy lực lượng phiến quân ra khỏi đây, nhưng sẽ không tiến hành ngay lập tức hoạt động quân sự tại các vùng liền kề đó.</p>
<p>Xác máy bay Boeing tại hiện trường hầu như không được bảo vệ. Nhiều bộ phận bị di chuyển hoặc tháo dỡ. Phiến quân cho biết sự can thiệp hiện trường này là nhằm mục đích tìm kiếm các thi thể. Chưa một hoạt động giám định pháp y nào diễn ra để đảm bảo rằng tất cả các thi thể nạn nhân đã được thu thập.</p>
<p>Tổ chức An ninh và Hợp tác châu Âu cho biết, nhóm điều tra của họ cùng các chuyên gia từ Australia và Hà Lan đã buộc phải quay trở lại Donetsk vì "lý do an ninh".</p>
<p>Thủ lĩnh phe nổi dậy Vladimir Antyufeyev cho biết, máy bay chiến đấu của phe ly khai hộ tống các chuyên gia quốc tế đến hiện trường phải quay đầu do xung đột vũ trang tại khu vực lân cận.</p>
<p>Dữ liệu thu thập được từ hộp đen của chiếc máy bay MH17 cho thấy thân phi cơ bị trúng rất nhiều mảnh đạn từ một vụ nổ tên lửa. Phe ly khai hôm qua cáo buộc chính phủ Ukraine gia tăng tấn công vào khu vực gần hiện trường MH17 nhằm xóa sạch dấu vết chứng minh Kiev đứng đằng sau vụ rơi máy bay của Malaysia. Tuy nhiên, Ukraine tuyên bố không kích hoạt hệ thống tên lửa nào vào thời điểm máy bay bị bắn.</p>
