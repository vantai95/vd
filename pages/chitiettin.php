<h1 class="title">Ukraine giành kiểm soát nhiều khu vực gần hiện trường MH17</h1>
<div class="des">
Quân đội Ukraine hôm nay chiếm lại nhiều phần lãnh thổ từ phe ly khai thân Nga, nhằm tiến tới giành quyền kiểm soát hiện trường vụ rơi máy bay MH17, nơi đội điều tra quốc tế không thể tiếp cận do giao tranh giữa hai bên.
